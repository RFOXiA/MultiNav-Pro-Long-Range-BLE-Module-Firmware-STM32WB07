/**
  * @brief  Initialize sensor DMA context
  * @param  sensor: Sensor ID
  * @retval None
  */
static void DMA_InitSensorContext(SensorID_t sensor)
{
    DMA_SensorContext_t *ctx = &sensorContexts[sensor];
    
    ctx->state = DMA_TRANSFER_IDLE;
    ctx->retryCount = 0;
    
    switch (sensor) {
        case SENSOR_BMI270:
            ctx->hi2c = &hi2c2;
            ctx->devAddr = 0x68 << 1;
            ctx->regAddr = 0x0C;  // Gyro/Accel data register
            ctx->pRawData = bmi270_raw_buffer;
            ctx->pBlePacket = ble_bmi270_gyro_packet;  // Will format both gyro and accel
            ctx->dataSize = 12;
            ctx->timeout = DMA_TIMEOUT_BMI270;
            break;
            
        case SENSOR_MVH4000D:
            ctx->hi2c = &hi2c2;
            ctx->devAddr = 0xA8;  // Already shifted in sensor init
            ctx->regAddr = 0x00;
            ctx->pRawData = mvh4000d_raw_buffer;
            ctx->pBlePacket = ble_mvh4000d_temp_packet;
            ctx->dataSize = 4;
            ctx->timeout = DMA_TIMEOUT_MVH4000D;
            break;
            
        case SENSOR_LPS22HH:
            ctx->hi2c = &hi2c2;
            ctx->devAddr = 0xB8;  // Already shifted in sensor init
            ctx->regAddr = 0x28;  // Pressure data register
            ctx->pRawData = lps22hh_raw_buffer;
            ctx->pBlePacket = ble_lps22hh_press_packet;
            ctx->dataSize = 3;
            ctx->timeout = DMA_TIMEOUT_LPS22HH;
            break;
            
        case SENSOR_ZMOD4510:
            ctx->hi2c = &hi2c1;
            ctx->devAddr = 0x32 << 1;
            ctx->regAddr = 0x00;
            ctx->pRawData = zmod4510_raw_buffer;
            ctx->pBlePacket = ble_zmod4510_aqi_packet;
            ctx->dataSize = 7;
            ctx->timeout = DMA_TIMEOUT_ZMOD4510;
            break;
            
        case SENSOR_GNSS:
            ctx->hi2c = &hi2c1;
            ctx->devAddr = 0x42 << 1;
            ctx->regAddr = 0xFF;  // GNSS uses special protocol
            ctx->pRawData = gnss_raw_buffer;
            ctx->pBlePacket = ble_gnss_packet;
            ctx->dataSize = 20;
            ctx->timeout = DMA_TIMEOUT_GNSS;
            break;
            
        default:
            break;
    }
}

/**
  * @brief  Get sensor ID from DMA handle
  * @param  hdma: DMA handle
  * @retval Sensor ID or SENSOR_COUNT if not found
  */
static SensorID_t DMA_GetSensorFromHandle(DMA_HandleTypeDef *hdma)
{
    extern uint8_t Scheduler_IsSensorEnabled(SensorID_t sensor);
    
    for (uint8_t i = 0; i < SENSOR_COUNT; i++) {
        /* Skip disabled sensors in lookup */
        if (!Scheduler_IsSensorEnabled((SensorID_t)i)) {
            continue;
        }
        
        if (sensorContexts[i].hi2c->hdmarx == hdma) {
            return (SensorID_t)i;
        }
    }
    return SENSOR_COUNT;
}

