/**
  * @brief  Initialize all DMA channels for autonomous operation
  * @note   STM32WB07-specific DMA initialization generated by CubeMX
  * @retval HAL status
  */
HAL_StatusTypeDef DMA_AutonomousSystem_Init(void)
{
    /* Enable DMA controller clock */
    __HAL_RCC_DMA_CLK_ENABLE();

    /* DMA interrupt init */
    /* DMA_IRQn interrupt configuration */
    HAL_NVIC_SetPriority(DMA_IRQn, 1, 0);
    HAL_NVIC_EnableIRQ(DMA_IRQn);
    
    /* Initialize sensor contexts for all sensors */
    DMA_InitAllSensorContexts();
    
    /* NOTE: DMA-to-I2C linking happens in HAL_I2C_MspInit() */
    /* DMA handles are configured when I2C peripherals are initialized */
    
    return HAL_OK;
}

/**
  * @brief  Legacy MX_DMA_Init function for compatibility
  * @note   Calls DMA_AutonomousSystem_Init()
  */
HAL_StatusTypeDef MX_DMA_Init(void)
{
    return DMA_AutonomousSystem_Init();
}

/**
  * @brief  Initialize sensor contexts for autonomous DMA operation
  * @note   Called internally after DMA init
  * @retval None
  */
static void DMA_InitAllSensorContexts(void)
{
    DMA_InitSensorContext(SENSOR_BMI270);
    DMA_InitSensorContext(SENSOR_MVH4000D);
    DMA_InitSensorContext(SENSOR_LPS22HH);
    DMA_InitSensorContext(SENSOR_ZMOD4510);
    DMA_InitSensorContext(SENSOR_GNSS);
}

/**
  * @brief  De-initialize DMA system
  * @retval HAL status
  */
HAL_StatusTypeDef DMA_AutonomousSystem_DeInit(void)
{
    HAL_DMA_DeInit(&hdma_i2c1_tx);
    HAL_DMA_DeInit(&hdma_i2c1_rx);
    HAL_DMA_DeInit(&hdma_i2c2_tx);
    HAL_DMA_DeInit(&hdma_i2c2_rx);
    HAL_DMA_DeInit(&hdma_mem2mem);
    
    return HAL_OK;
}

/**
  * @brief  Start autonomous sensor read via DMA
  * @param  sensor: Sensor ID
  * @retval HAL status
  */
HAL_StatusTypeDef DMA_Sensor_StartRead(SensorID_t sensor)
{
    HAL_StatusTypeDef status;
    DMA_SensorContext_t *ctx = &sensorContexts[sensor];
    
    /* Check if sensor is enabled before starting */
    extern uint8_t Scheduler_IsSensorEnabled(SensorID_t sensor);
    if (!Scheduler_IsSensorEnabled(sensor)) {
        return HAL_ERROR;  // Don't start DMA for disabled sensors
    }
    
    /* Check if previous transfer is still busy */
    if (ctx->state == DMA_TRANSFER_BUSY) {
        return HAL_BUSY;
    }
    
    /* Update state (allow retry even if previous was error) */
    ctx->state = DMA_TRANSFER_BUSY;
    ctx->startTime = HAL_GetTick();
    
    /* Start I2C read with DMA */
    status = HAL_I2C_Mem_Read_DMA(ctx->hi2c, 
                                   ctx->devAddr, 
                                   ctx->regAddr, 
                                   I2C_MEMADD_SIZE_8BIT,
                                   ctx->pRawData, 
                                   ctx->dataSize);
    
    if (status != HAL_OK) {
        ctx->state = DMA_TRANSFER_IDLE;  // Reset to IDLE for next attempt
        errorCount[sensor]++;
        printf("[DMA-ERR] S%d start failed: %d\r\n", sensor, status);
        return status;
    }
    
    return HAL_OK;
}

/**
  * @brief  Abort sensor DMA transfer
  * @param  sensor: Sensor ID
  * @retval HAL status
  */
HAL_StatusTypeDef DMA_Sensor_AbortRead(SensorID_t sensor)
{
    DMA_SensorContext_t *ctx = &sensorContexts[sensor];
    
    HAL_I2C_Master_Abort_IT(ctx->hi2c, ctx->devAddr);
    ctx->state = DMA_TRANSFER_IDLE;
    
    return HAL_OK;
}

/**
  * @brief  Check if sensor data is ready
  * @param  sensor: Sensor ID
  * @retval 1 if ready, 0 if not ready
  */
uint8_t DMA_Sensor_IsDataReady(SensorID_t sensor)
{
    return (sensorContexts[sensor].state == DMA_TRANSFER_COMPLETE);
}

/**
  * @brief  Get sensor DMA context
  * @param  sensor: Sensor ID
  * @retval Pointer to DMA context
  */
DMA_SensorContext_t* DMA_Sensor_GetContext(SensorID_t sensor)
{
    return &sensorContexts[sensor];
}

/**
  * @brief  Format raw sensor data to BLE packet (via DMA Mem2Mem)
  * @param  sensor: Sensor ID
  * @retval HAL status
  */
HAL_StatusTypeDef DMA_FormatToBLEPacket(SensorID_t sensor)
{
    DMA_SensorContext_t *ctx = &sensorContexts[sensor];
    
    /* For most sensors, raw data is already in correct format */
    /* Just copy via DMA Mem2Mem */
    return HAL_DMA_Start(&hdma_mem2mem,
                         (uint32_t)ctx->pRawData,
                         (uint32_t)ctx->pBlePacket,
                         ctx->dataSize);
}

/**
  * @brief  DMA transfer complete callback
  * @param  hdma: DMA handle
  * @retval None
  */
void DMA_TransferComplete_Callback(DMA_HandleTypeDef *hdma)
{
    SensorID_t sensor = DMA_GetSensorFromHandle(hdma);
    
    if (sensor < SENSOR_COUNT) {
        DMA_SensorContext_t *ctx = &sensorContexts[sensor];
        ctx->state = DMA_TRANSFER_COMPLETE;
        
        /* Automatically format to BLE packet */
        DMA_FormatToBLEPacket(sensor);
    }
}

/**
  * @brief  DMA transfer error callback
  * @param  hdma: DMA handle
  * @retval None
  */
void DMA_TransferError_Callback(DMA_HandleTypeDef *hdma)
{
    SensorID_t sensor = DMA_GetSensorFromHandle(hdma);
    
    if (sensor < SENSOR_COUNT) {
        DMA_SensorContext_t *ctx = &sensorContexts[sensor];
        ctx->state = DMA_TRANSFER_ERROR;
        errorCount[sensor]++;
        
        /* Check if sensor is still enabled before auto-retry */
        extern uint8_t Scheduler_IsSensorEnabled(SensorID_t sensor);
        if (!Scheduler_IsSensorEnabled(sensor)) {
            printf("[DMA] Sensor %d disabled, not retrying\r\n", sensor);
            ctx->retryCount = 0;
            ctx->state = DMA_TRANSFER_IDLE;
            return;
        }
        
        /* Auto-retry if not exceeded max retries */
        if (ctx->retryCount < DMA_MAX_RETRIES) {
            ctx->retryCount++;
            DMA_Sensor_StartRead(sensor);
        } else {
            ctx->retryCount = 0;
        }
    }
}

/**
  * @brief  Monitor DMA timeouts (call from scheduler)
  * @retval None
  */
void DMA_CheckTimeouts(void)
{
    uint32_t now = HAL_GetTick();
    
    for (uint8_t i = 0; i < SENSOR_COUNT; i++) {
        DMA_SensorContext_t *ctx = &sensorContexts[i];
        
        if (ctx->state == DMA_TRANSFER_BUSY) {
            if ((now - ctx->startTime) > ctx->timeout) {
                ctx->state = DMA_TRANSFER_TIMEOUT;
                timeoutCount[i]++;
                DMA_ResetChannel((SensorID_t)i);
            }
        }
    }
}

/**
  * @brief  Reset DMA channel and I2C peripheral
  * @param  sensor: Sensor ID
  * @retval None
  */
void DMA_ResetChannel(SensorID_t sensor)
{
    DMA_SensorContext_t *ctx = &sensorContexts[sensor];
    
    /* Abort DMA transfer */
    HAL_DMA_Abort(ctx->hi2c->hdmarx);
    HAL_DMA_Abort(ctx->hi2c->hdmatx);
    
    /* Reset I2C peripheral */
    if (ctx->hi2c == &hi2c1) {
        __HAL_RCC_I2C1_FORCE_RESET();
        HAL_Delay(1);
        __HAL_RCC_I2C1_RELEASE_RESET();
    } else if (ctx->hi2c == &hi2c2) {
        __HAL_RCC_I2C2_FORCE_RESET();
        HAL_Delay(1);
        __HAL_RCC_I2C2_RELEASE_RESET();
    }
    
    /* Re-initialize I2C */
    HAL_I2C_Init(ctx->hi2c);
    
    /* Reset context */
    ctx->state = DMA_TRANSFER_IDLE;
    ctx->retryCount = 0;
}

/**
  * @brief  Get DMA error statistics
  */
void DMA_GetErrorStats(SensorID_t sensor, uint32_t *pErrorCount, uint32_t *pTimeoutCount)
{
    if (pErrorCount) *pErrorCount = errorCount[sensor];
    if (pTimeoutCount) *pTimeoutCount = timeoutCount[sensor];
}

